%η finddif βρίσκει τις διαφορές μεταξύ δύο λιστών
findDif([ ],_,[ ]).
findDif([H|T],L,K) :- %η λίστα [H|T] έχει όλα τα μαθήματα του τρέχων εξαμήνου και η λίστα L τα περασμένα μαθήματα του αντίστοιχου εξαμήνου 
    member(H,L), %αν το H δεν υπάρχει στην λίστα L,δηλαδή αν δεν το έχω περάσει
    !, %σταμάτα και κάλεσε το άλλο κατηγόρημα findDif
    findDif(T,L,K). %αλλιώς προχώρα στην ουρά T
findDif([H|T],L,[H|K]) :- %πρόσθεσε το H στην λίστα K
    findDif(T,L,K). %και προχώρα στην ουρά
    